#ifndef __SSDF_DSP_COMMON_INCLUDE_SC_TYPES_H__
#define __SSDF_DSP_COMMON_INCLUDE_SC_TYPES_H__

/// Basic types for using SSDF
typedef int                 sc_s32;
typedef unsigned            sc_u32;
typedef short               sc_s16;
typedef unsigned short      sc_u16;
typedef char                sc_s8;
typedef unsigned char       sc_u8;
typedef long long           sc_s64;
typedef unsigned long long  sc_u64;

/// Extended types for SCore-CV
typedef sc_s8               sc_char;
typedef sc_s32              sc_enum;
typedef float               sc_float32;
typedef double              sc_float64;
typedef sc_u32              sc_fourcc;
typedef sc_s16              sc_int16;
typedef sc_s32              sc_int32;
typedef sc_s64              sc_int64;
typedef sc_s8               sc_int8;
typedef sc_u32              sc_size;
typedef sc_u16              sc_uint16;
typedef sc_u32              sc_uint32;
typedef sc_s64              sc_uint64;
typedef sc_u8               sc_uint8;
typedef sc_u32              sc_scalar;
typedef unsigned int        sc_addr;

#endif
